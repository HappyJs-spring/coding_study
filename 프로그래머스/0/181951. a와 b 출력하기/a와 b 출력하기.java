import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        // nextInt(): 공백(스페이스, 탭, 엔터 등)을 기준으로 정수 하나를 읽는다
        int a = sc.nextInt();
        int b = sc.nextInt();

		System.out.println("a = " + a + "\nb = " + b);
    }
}